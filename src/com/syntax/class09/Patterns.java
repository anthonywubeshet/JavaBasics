package com.syntax.class09;

public class Patterns {

	public static void main(String[] args) {
		
		for(int a=1; a<=5; a++) {
			for(int b=1; b<=5; b++) {
				System.out.print("*");
			}
		}
		System.out.println();

		for(int i=0; i<=5; i++) {
			for(int j=1; j<=10; j++) {
				System.out.print("*");
			}
		}
		
		System.out.println();
		for(int r=1; r<=7; r++) {
			for(int c=1; c<=r; c++) {
				System.out.print(c);
			}
			System.out.println();
		}
		
		
			
		
	}}
