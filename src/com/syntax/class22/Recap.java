package com.syntax.class22;

public class Recap {

	public void hello() {
		System.out.println("hello");
	}
	
	public void hello(String name) {// this method is overloaded
		System.out.println("hello"+name);
	}
	
	public void hello(int name) {// this method is overloaded
		System.out.println("hello"+name);
	}
	
}
